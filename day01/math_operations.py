# Operaciones matemáticas básicas

# Números
a: int = 10
b: int = 3

# Operaciones
suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
division_entera = a // b
modulo = a % b
potencia = a ** b

# Imprimir resultados
print("a =", a)
print("b =", b)
print("---")
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("División entera:", division_entera)
print("Módulo (resto):", modulo)
print("Potencia:", potencia)